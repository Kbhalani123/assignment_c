//program to make simple calculator (to make addition,subtraction,multiplication,division,and modulo)

#include<stdio.h>
 int main(){
 int a,b;
 
 printf("enter the a:");
 scanf("%d",&a);
 
 printf("enter the b:");
 scanf("%d",&b);
 
 printf("addition:%d\n",a+b);
 printf("subtraction:%d\n",a-b);
 printf("multiplication:%d\n",a*b);
 printf("division:%d\n",a/b);
 printf("modulo:%d\n",a%b);

 return 0;
}
 
 
 